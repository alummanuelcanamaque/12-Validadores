    private void botonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActionPerformed
        CreditCardValidator validadorTarjeta = new CreditCardValidator();
        boolean validacionTarjeta = validadorTarjeta.isValid(numTarjeta.getText());
        String estadoTarjeta = (validacionTarjeta==true) ? "Correcto" : "Incorrecto";
        correcto1.setText(estadoTarjeta);
        
        ISBNValidator validadorIsbn = new ISBNValidator();
        boolean validacionIsbn = validadorIsbn.isValid(isbn.getText());
        String estadoIsbn = (validacionIsbn==true) ? "Correcto" : "Incorrecto";
        correcto2.setText(estadoIsbn);
        
        EmailValidator validadorEmail = EmailValidator.getInstance();
        boolean validacionEmail = validadorEmail.isValid(email.getText());
        String estadoEmail = (validacionEmail==true) ? "Correcto" : "Incorrecto";
        correcto3.setText(estadoEmail);
        
        
            
            
        
    }//GEN-LAST:event_botonActionPerformed

